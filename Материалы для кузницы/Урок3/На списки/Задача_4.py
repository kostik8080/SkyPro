"""Есть список. Выведите, сколько раз в  нем  встречаются элементы числа “1” и “2”

```python
numbers = [10, 4, 5, 1, 2, 10, 7, 8, 1, 2, 10]
```

Пример вывода:

4
"""

numbers = [10, 4, 5, 1, 2, 10, 7, 8, 1, 2, 10]

total = 0
for i in numbers:
    if i == 1:
        total += 1
    elif i == 2:
        total += 1
print(total)